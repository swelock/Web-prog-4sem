date_default_timezone_set('Europe/Moscow'); // Устанавливаем московский часовой пояс для корректного вывода даты и времени

$allowedHtmlTypes = ['TABLE', 'DIV']; // Создаём массив разрешённых типов вёрстки: табличная и блочная

$htmlTypeParam = null; // Создаём переменную для хранения реально переданного параметра html_type, сначала его нет

if (isset($_GET['html_type'])) { // Проверяем, был ли передан GET-параметр html_type в адресной строке
    $requestedHtmlType = strtoupper(trim((string)$_GET['html_type'])); // Берём значение html_type, убираем пробелы и переводим в верхний регистр

    if (in_array($requestedHtmlType, $allowedHtmlTypes, true)) { // Проверяем, входит ли переданное значение в список разрешённых типов
        $htmlTypeParam = $requestedHtmlType; // Если значение корректное, сохраняем его как выбранный пользователем тип вёрстки
    } // Закрываем проверку допустимого значения html_type
} // Закрываем проверку существования параметра html_type

// По умолчанию используется табличная верстка,
// но пункт верхнего меню при первой загрузке не выделяется.
$htmlType = ($htmlTypeParam !== null) ? $htmlTypeParam : 'TABLE'; // Если параметр передан — используем его, иначе по умолчанию ставим TABLE

$content = null; // Создаём переменную для хранения выбранного содержания таблицы, null означает «вся таблица»

if (isset($_GET['content'])) { // Проверяем, был ли передан GET-параметр content
    $requestedContent = (int)$_GET['content']; // Преобразуем значение content к целому числу

    if ($requestedContent >= 2 && $requestedContent <= 9) { // Проверяем, что число находится в допустимом диапазоне от 2 до 9
        $content = $requestedContent; // Если число корректное, сохраняем его как выбранный столбец таблицы умножения
    } // Закрываем проверку диапазона content
} // Закрываем проверку существования параметра content

function h($value) // Объявляем функцию h для безопасного вывода данных в HTML
{ // Открываем тело функции h
    return htmlspecialchars((string)$value, ENT_QUOTES, 'UTF-8'); // Преобразуем специальные символы, чтобы защитить HTML от некорректного вывода
} // Закрываем функцию h

function buildUrl($htmlType = null, $content = null) // Объявляем функцию формирования ссылок с GET-параметрами
{ // Открываем тело функции buildUrl
    $params = []; // Создаём пустой массив, куда будем добавлять параметры ссылки

    if ($htmlType !== null) { // Проверяем, нужно ли добавить параметр типа вёрстки
        $params['html_type'] = $htmlType; // Добавляем в массив параметр html_type
    } // Закрываем проверку htmlType

    if ($content !== null) { // Проверяем, нужно ли добавить параметр содержания таблицы
        $params['content'] = $content; // Добавляем в массив параметр content
    } // Закрываем проверку content

    if (count($params) === 0) { // Проверяем, пустой ли массив параметров
        return 'index.php'; // Если параметров нет, возвращаем ссылку просто на index.php
    } // Закрываем проверку пустого массива параметров

    return 'index.php?' . http_build_query($params); // Формируем ссылку вида index.php?html_type=TABLE&content=4
} // Закрываем функцию buildUrl

function selectedClass($condition) // Объявляем функцию для добавления CSS-класса selected
{ // Открываем тело функции selectedClass
    return $condition ? ' class="selected"' : ''; // Если условие истинно — возвращаем class="selected", иначе пустую строку
} // Закрываем функцию selectedClass

// Все цифры от 2 до 9 делаем ссылками.
// Внутри таблицы ссылка НЕ передает html_type, чтобы тип верстки сбрасывался.
function outNumAsLink($number) // Объявляем функцию, которая превращает число в ссылку, если это возможно
{ // Открываем тело функции outNumAsLink
    if ($number >= 2 && $number <= 9) { // Проверяем, находится ли число в диапазоне от 2 до 9
        return '<a class="number-link" href="' . h(buildUrl(null, $number)) . '">' . h($number) . '</a>'; // Возвращаем число как ссылку на соответствующую таблицу умножения
    } // Закрываем проверку диапазона числа

    return h($number); // Если число больше 9, возвращаем его обычным текстом без ссылки
} // Закрываем функцию outNumAsLink

function outRow($n) // Объявляем функцию вывода одного столбца таблицы умножения
{ // Открываем тело функции outRow
    for ($i = 2; $i <= 9; $i++) { // Запускаем цикл от 2 до 9 для формирования строк умножения
        $result = $n * $i; // Вычисляем результат умножения выбранного числа на текущее значение цикла

        echo '<div class="example-line">'; // Открываем блок одной строки примера умножения
        echo outNumAsLink($n); // Выводим первое число как ссылку, если оно от 2 до 9
        echo '<span class="sign">×</span>'; // Выводим знак умножения
        echo outNumAsLink($i); // Выводим второе число как ссылку
        echo '<span class="sign">=</span>'; // Выводим знак равенства
        echo outNumAsLink($result); // Выводим результат как ссылку, если он от 2 до 9, иначе обычным числом
        echo '</div>'; // Закрываем блок одной строки примера
    } // Закрываем цикл вывода строк умножения
} // Закрываем функцию outRow

// Табличная верстка.
// Полная таблица всегда выводится в 2 ряда по 4 колонки.
function outTableForm($content) // Объявляем функцию вывода таблицы умножения в табличной форме
{ // Открываем тело функции outTableForm
    if ($content === null) { // Проверяем, нужно ли вывести всю таблицу полностью
        echo '<table class="multiplication-table full-table">'; // Открываем HTML-таблицу для полной таблицы умножения
        echo '<tbody>'; // Открываем тело таблицы

        // Первый ряд:  полной таблицы умножения
        echo '<tbody>'; // Открываем тело таблицы

        // Первый ряд: 2, 3, 4, 5
        // Второй ряд: 6, 7, 8, 9
        for ($rowStart = 2; $rowStart <= 6; $rowStart += 4) { // Цикл создаёт два ряда: первый начинается с 2, второй с 6
            echo '<tr>'; // Открываем строку таблицы

            for ($i = $rowStart; $i < $rowStart + 4; $i++) { // Внутренний цикл выводит 4 столбца в одном ряду
                echo '<td>'; // Открываем ячейку таблицы
                echo '<div class="column-title">На ' . h($i) . '</div>'; // Выводим заголовок столбца, например «На 4»
                outRow($i); // Вызываем функцию вывода таблицы умножения на число i
                echo '</td>'; // Закрываем ячейку таблицы
            } // Закрываем внутренний цикл вывода четырёх столбцов

            echo '</tr>'; // Закрываем строку таблицы
        } // Закрываем внешний цикл вывода двух рядов

        echo '</tbody>'; // Закрываем тело таблицы
        echo '</table>'; // Закрываем HTML-таблицу
    } else { // Если выбран конкретный столбец, а не вся таблица
        echo '<table class="multiplication-table single-table">'; // Открываем таблицу для одного крупного столбца
        echo '<tbody>'; // Открываем тело таблицы
        echo '<tr>'; // Открываем строку таблицы
        echo '<td>'; // Открываем ячейку таблицы
        echo '<div class="column-title">Таблица умножения на ' . h($content) . '</div>'; // Выводим заголовок выбранного столбца
        outRow($content); // Выводим таблицу умножения на выбранное число
        echo '</td>'; // Закрываем ячейку таблицы
        echo '</tr>'; // Закрываем строку таблицы
        echo '</tbody>'; // Закрываем тело таблицы
        echo '</table>'; // Закрываем таблицу
    } // Закрываем условие выбора полной таблицы или одного столбца
} // Закрываем функцию outTableForm

// Блочная верстка.
// Полная таблица тоже всегда выводится сеткой 4 на 2.
function outDivForm($content) // Объявляем функцию вывода таблицы умножения в блочной форме
{ // Открываем тело функции outDivForm
    if ($content === null) { // Проверяем, нужно ли вывести всю таблицу полностью
        echo '<div class="multiplication-div full-div">'; // Открываем общий блок для полной блочной таблицы

        for ($i = 2; $i <= 9; $i++) { // Цикл проходит по всем числам от 2 до 9
            echo '<div class="div-column">'; // Открываем отдельный блок-колонку
            echo '<div class="column-title">На ' . h($i) . '</div>'; // Выводим заголовок колонки
            outRow($i); // Выводим таблицу умножения на текущее число
            echo '</div>'; // Закрываем блок-колонку
        } // Закрываем цикл вывода колонок

        echo '</div>'; // Закрываем общий блок полной таблицы
    } else { // Если выбран конкретный столбец
        echo '<div class="multiplication-div single-div">'; // Открываем общий блок для одного столбца
        echo '<div class="div-column">'; // Открываем блок-колонку
        echo '<div class="column-title">Таблица умножения на ' . h($content) . '</div>'; // Выводим заголовок выбранной таблицы
        outRow($content); // Выводим таблицу умножения на выбранное число
        echo '</div>'; // Закрываем блок-колонку
        echo '</div>'; // Закрываем общий блок одного столбца
    } // Закрываем условие выбора полной таблицы или одного столбца
} // Закрываем функцию outDivForm

function outMainMenu($htmlTypeParam, $content) // Объявляем функцию вывода верхнего меню выбора типа вёрстки
{ // Открываем тело функции outMainMenu
    echo '<nav class="main-menu">'; // Открываем навигационный блок верхнего меню
    echo '<a href="' . h(buildUrl('TABLE', $content)) . '"' . selectedClass($htmlTypeParam === 'TABLE') . '>Табличная верстка</a>'; // Выводим ссылку на табличную вёрстку и сохраняем выбранный content
    echo '<a href="' . h(buildUrl('DIV', $content)) . '"' . selectedClass($htmlTypeParam === 'DIV') . '>Блочная верстка</a>'; // Выводим ссылку на блочную вёрстку и сохраняем выбранный content
    echo '</nav>'; // Закрываем верхнее меню
} // Закрываем функцию outMainMenu

function outProductMenu($htmlTypeParam, $content) // Объявляем функцию вывода бокового меню выбора содержания таблицы
{ // Открываем тело функции outProductMenu
    echo '<aside class="product-menu">'; // Открываем боковое меню
    echo '<div class="side-title">Основное меню</div>'; // Выводим заголовок бокового меню

    echo '<a href="' . h(buildUrl($htmlTypeParam, null)) . '"' . selectedClass($content === null) . '>Всё</a>'; // Выводим пункт «Всё», сохраняя выбранный тип вёрстки

    for ($i = 2; $i <= 9; $i++) { // Запускаем цикл для пунктов меню от 2 до 9
        echo '<a href="' . h(buildUrl($htmlTypeParam, $i)) . '"' . selectedClass($content === $i) . '>' . h($i) . '</a>'; // Выводим ссылку на таблицу умножения на текущее число
    } // Закрываем цикл вывода пунктов бокового меню

    echo '</aside>'; // Закрываем боковое меню
} // Закрываем функцию outProductMenu

function getLayoutTitle($htmlType) // Объявляем функцию получения текстового названия типа вёрстки
{ // Открываем тело функции getLayoutTitle
    return $htmlType === 'DIV' ? 'Блочная верстка' : 'Табличная верстка'; // Если тип DIV — возвращаем блочную, иначе табличную
} // Закрываем функцию getLayoutTitle

function getContentTitle($content) // Объявляем функцию получения названия текущего содержания страницы
{ // Открываем тело функции getContentTitle
    if ($content === null) { // Проверяем, выбрана ли полная таблица
        return 'Таблица умножения полностью'; // Если content нет, возвращаем название полной таблицы
    } // Закрываем проверку полной таблицы

    return 'Столбец таблицы умножения на ' . $content; // Если выбран столбец, возвращаем название с номером столбца
} // Закрываем функцию getContentTitle
