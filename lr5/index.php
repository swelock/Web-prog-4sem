    <?php outProductMenu($htmlTypeParam, $content); ?> <!-- Вызываем PHP-функцию вывода левого меню -->

    <section class="content-card"> <!-- Открываем карточку с основным содержимым -->
        <h2><?php echo h(getContentTitle($content)); ?></h2> <!-- Выводим заголовок: вся таблица или конкретный столбец -->

        <p class="description"> <!-- Открываем абзац с описанием работы страницы -->
            Тип вёрстки и содержание таблицы выбираются через GET-параметры. <!-- Первая строка пояснения -->
            При смене типа вёрстки выбранный столбец сохраняется, <!-- Вторая строка пояснения -->
            а при выборе пункта левого меню сохраняется выбранный тип вёрстки. <!-- Третья строка пояснения -->
        </p> <!-- Закрываем абзац описания -->

        <?php // Открываем PHP-блок для выбора функции вывода таблицы
        if ($htmlType === 'DIV') { // Проверяем, выбран ли блочный тип вёрстки
            outDivForm($content); // Если выбран DIV, выводим таблицу через блоки div
        } else { // Иначе используется табличная вёрстка
            outTableForm($content); // Выводим таблицу через HTML-таблицу table
        } // Закрываем условие выбора типа вывода таблицы
        ?> <!-- Закрываем PHP-блок -->
