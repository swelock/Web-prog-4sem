    $sum_f = 0; // Переменная для суммы всех числовых значений функции
    $valid_count = 0; // Счётчик корректных числовых значений, error сюда не входит
    $stop_reason = 'Вычисления завершены по достижении заданного количества значений.'; // Текст причины завершения вычислений по умолчанию

    // -------------------- ФУНКЦИЯ ВАРИАНТА №8 --------------------

    function calculateFunctionVariant8(float $x): float|string // Объявляем функцию для вычисления значения функции по варианту 8
    { // Открываем тело функции
        // Вариант №8:
        // 1) f(x) = 7*x + 18, если x <= 10
        // 2) f(x) = (x - 17) / (8 - x*0.5), если x > 10 и x < 20
        // 3) f(x) = (x + 4) * (x - 7), если x >= 20

        if ($x <= 10) { // Проверяем первую область: если x меньше или равен 10
            return 7 * $x + 18; // Возвращаем значение первой формулы f(x) = 7x + 18
        } // Закрываем первую ветку условия

        if ($x > 10 && $x < 20) { // Проверяем вторую область: если x больше 10 и меньше 20
            $denominator = 8 - $x * 0.5; // Вычисляем знаменатель второй формулы

            // При x = 16 знаменатель равен нулю: 8 - 16 * 0.5 = 0

            if (abs($denominator) < 0.0000001) { // Проверяем, не стал ли знаменатель равен нулю
                return 'error'; // Если делить нельзя, возвращаем строку error
            } // Закрываем проверку деления на ноль

            return ($x - 17) / $denominator; // Если знаменатель не равен нулю, возвращаем значение второй формулы
        } // Закрываем вторую ветку условия

        return ($x + 4) * ($x - 7); // Если x >= 20, возвращаем значение третьей формулы
    } // Закрываем функцию вычисления варианта 8

    function formatValue(float|int|string|null $value): string // Объявляем функцию форматирования значения перед выводом
    { // Открываем тело функции форматирования
        if ($value === null) { // Проверяем, отсутствует ли значение
            return 'нет данных'; // Если значения нет, выводим текст "нет данных"
        } // Закрываем проверку null

        if ($value === 'error') { // Проверяем, является ли значение строкой error
            return 'error'; // Если это error, возвращаем его без изменений
        } // Закрываем проверку error

        $rounded = round((float)$value, 3); // Преобразуем значение к числу и округляем до 3 знаков после запятой

        // Убираем возможное значение -0.000, чтобы вывод был математически аккуратным.

        if (abs($rounded) < 0.0005) { // Проверяем, является ли число практически нулём
            $rounded = 0; // Если число очень близко к нулю, заменяем его на обычный 0
        } // Закрываем проверку около нуля

        return number_format($rounded, 3, '.', ''); // Возвращаем число в виде строки с тремя знаками после точки
    } // Закрываем функцию форматирования

    // -------------------- ОСНОВНОЙ ЦИКЛ --------------------

    // Выбран цикл for, потому что заранее известно максимальное количество итераций,
    // а изменение счетчика и аргумента удобно записывается прямо в заголовке цикла.

    for ($i = 0, $x = $x_start; $i < $encounting; $i++, $x += $step) { // Запускаем цикл: i — счётчик, x — текущий аргумент
