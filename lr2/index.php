    $step = 2;               // Задаём шаг изменения аргумента x, то есть x будет увеличиваться на 2
    $min_value = -1000;      // Задаём нижнюю границу значения функции для досрочной остановки
    $max_value = 1000;       // Задаём верхнюю границу значения функции для досрочной остановки
    $type = 'E';             // Задаём тип верстки по умолчанию: A, B, C, D или E

    // Дополнительная возможность для проверки всех типов верстки через адресную строку:
