            break; // Досрочно останавливаем цикл
        } // Закрываем проверку досрочной остановки
    } // Закрываем основной цикл for

    $average_f = $valid_count > 0 ? round($sum_f / $valid_count, 3) : null; // Считаем среднее, если есть хотя бы одно числовое значение
    $sum_f = $valid_count > 0 ? round($sum_f, 3) : null; // Округляем сумму, если есть числовые значения
