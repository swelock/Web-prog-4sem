        if ($f !== 'error') { // Проверяем, что результат является числом, а не строкой error
            $f = round((float)$f, 3); // Округляем числовое значение функции до 3 знаков после запятой

            if (abs($f) < 0.0005) { // Проверяем, не получилось ли значение типа -0.000
                $f = 0; // Если значение почти ноль, заменяем его на 0
            } // Закрываем проверку почти нулевого значения

            if ($max_f === null || $f > $max_f) { // Если максимума ещё нет или текущее значение больше максимума
                $max_f = $f; // Записываем новое максимальное значение функции
            } // Закрываем проверку максимума

            if ($min_f === null || $f < $min_f) { // Если минимума ещё нет или текущее значение меньше минимума
                $min_f = $f; // Записываем новое минимальное значение функции
            } // Закрываем проверку минимума

            $sum_f += $f; // Добавляем текущее значение функции к общей сумме
            $valid_count++; // Увеличиваем счётчик корректных числовых значений
        } // Закрываем блок обработки числового результата

        $results[] = [ // Добавляем текущий результат в массив результатов
            'number' => $i + 1, // Сохраняем номер строки, начинаем с 1, поэтому i + 1
            'x' => $x, // Сохраняем текущее значение аргумента x
            'f' => $f // Сохраняем значение функции f(x)
        ]; // Закрываем массив одной строки результата

        // Если полученное числовое значение функции вышло за заданные границы,
        // вычисления останавливаются досрочно.

        if ($f !== 'error' && ($f >= $max_value || $f <= $min_value)) { // Проверяем выход значения функции за границы
