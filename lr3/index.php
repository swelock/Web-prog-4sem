<?php
// Открываем PHP-блок, здесь будет выполняться серверная логика до вывода HTML-страницы

// Инициализация хранилища, то есть строки, которая отображается в окне результата
if (!isset($_GET['store'])) {
    // Проверяем: если GET-параметр store НЕ был передан в адресной строке

    $store = '';
    // Создаём пустую строку, потому что это первая загрузка страницы или сброс
} else {
    // Иначе, если параметр store уже был передан

    $store = $_GET['store'];
    // Записываем в переменную $store предыдущее значение строки из GET-параметра
}

// Инициализация счётчика нажатий
if (!isset($_GET['clicks'])) {
    // Проверяем: если GET-параметр clicks НЕ был передан

    $clicks = 0;
    // Устанавливаем количество нажатий равным нулю
} else {
    // Иначе, если параметр clicks уже был передан

    $clicks = (int)$_GET['clicks'];
    // Получаем значение clicks из адресной строки и приводим его к целому числу
}

// Обработка нажатия кнопки
if (isset($_GET['key'])) {
    // Проверяем: если параметр key передан, значит была нажата какая-то кнопка

    $clicks++;
    // Увеличиваем счётчик нажатий на 1, так как нажата любая кнопка

    if ($_GET['key'] === 'reset') {
        // Проверяем: если значение key равно reset, значит нажата кнопка СБРОС

        $store = '';
        // Очищаем строку результата
    } else {
        // Иначе была нажата не кнопка сброса, а кнопка с цифрой

        $store .= htmlspecialchars($_GET['key']);
        // Добавляем нажатую цифру в конец строки
        // htmlspecialchars защищает вывод от возможной вставки HTML-кода через URL
    }
}
?>
<!-- Закрываем PHP-блок и начинаем HTML-разметку страницы -->

<!DOCTYPE html>
<!-- Указываем браузеру, что используется современный стандарт HTML5 -->

<html lang="ru">
<!-- Открываем HTML-документ и указываем, что язык страницы русский -->

<head>
    <!-- Открываем служебную часть документа, которая не отображается напрямую на странице -->

    <meta charset="UTF-8">
    <!-- Указываем кодировку UTF-8, чтобы корректно отображались русские буквы -->

    <meta name="viewport" content="width=device-width, initial-scale=1.0">
    <!-- Настраиваем адаптивность страницы для разных экранов и мобильных устройств -->

    <title>Ильин Кирилл Александрович, Группа 241-353, Лабораторная работа №3</title>
    <!-- Задаём заголовок вкладки браузера -->

    <link rel="stylesheet" href="styles.css">
    <!-- Подключаем внешний CSS-файл со стилями страницы -->
</head>
<!-- Закрываем служебную часть документа -->

<body>
<!-- Открываем тело страницы, здесь находится всё видимое содержимое -->

    <header>
        <!-- Шапка сайта с логотипом и информацией о студенте -->

        <img src="https://upload.wikimedia.org/wikipedia/commons/9/90/LogoMospolytech.jpg" alt="Логотип Московского Политеха" class="logo">
        <!-- Выводим логотип Московского Политеха, class="logo" нужен для оформления через CSS -->

        <div class="info">
            <!-- Блок с информацией о студенте и лабораторной работе -->

            <h1>Ильин Кирилл Александрович</h1>
            <!-- Главный заголовок с ФИО студента -->

            <p>Группа: 241-353 | Лабораторная работа №3 | Виртуальная клавиатура</p>
            <!-- Абзац с номером группы, номером лабораторной работы и темой -->
        </div>
        <!-- Закрываем блок с информацией -->
    </header>
    <!-- Закрываем шапку сайта -->

    <main>
        <!-- Основная часть страницы -->

        <!-- Окно просмотра результата -->
        <div class="result"><?php echo $store; ?></div>
        <!-- Блок результата, в который PHP выводит текущее значение переменной $store -->

        <!-- Виртуальная клавиатура -->
        <div class="keyboard">
            <!-- Контейнер для кнопок виртуальной клавиатуры -->

            <?php
            // Открываем PHP-блок внутри HTML, чтобы динамически создать кнопки

            // Кнопки от 1 до 9
            for ($i = 1; $i <= 9; $i++) {
                // Запускаем цикл for: переменная $i идёт от 1 до 9

                echo "<a href=\"?key={$i}&store={$store}&clicks={$clicks}\">{$i}</a>";
                // Выводим ссылку-кнопку с цифрой
                // key={$i} передаёт нажатую цифру
                // store={$store} передаёт текущее содержимое результата
                // clicks={$clicks} передаёт текущее количество нажатий
            }
            // Заканчиваем цикл после создания кнопок 1, 2, 3, 4, 5, 6, 7, 8, 9
            ?>
            <!-- Закрываем PHP-блок -->

            <!-- Пустая ячейка для выравнивания нуля по центру -->
            <div></div>
            <!-- Пустой блок занимает одну ячейку сетки слева от кнопки 0 -->

            <!-- Кнопка 0 -->
            <a href="?key=0&store=<?php echo $store; ?>&clicks=<?php echo $clicks; ?>">0</a>
            <!-- Ссылка-кнопка 0, при нажатии передаёт key=0, store и clicks -->

            <div></div>
            <!-- Пустой блок занимает одну ячейку сетки справа от кнопки 0 -->

            <!-- Кнопка СБРОС -->
            <a href="?key=reset&store=<?php echo $store; ?>&clicks=<?php echo $clicks; ?>" class="reset">СБРОС</a>
            <!-- Ссылка-кнопка сброса, передаёт key=reset -->
            <!-- class="reset" нужен для отдельного оформления кнопки через CSS -->
        </div>
        <!-- Закрываем контейнер виртуальной клавиатуры -->
    </main>
    <!-- Закрываем основную часть страницы -->

    <footer>
        <!-- Подвал страницы -->

        <p>Общее число нажатий кнопок: <strong><?php echo $clicks; ?></strong></p>
        <!-- Выводим количество нажатий, значение берётся из переменной $clicks -->
    </footer>
    <!-- Закрываем подвал страницы -->

</body>
<!-- Закрываем тело страницы -->

</html>
<!-- Закрываем HTML-документ -->