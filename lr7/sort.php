// Сортировка выбором.
function sorting_by_choice(array $arr): int // Объявляем функцию сортировки выбором, возвращающую число итераций
{ // Открываем тело функции sorting_by_choice
    $iteration = 0; // Создаём счётчик итераций и начинаем с нуля
    echo '<table class="steps-table"><tr><th>№ итерации</th><th>Действие</th><th>Состояние массива</th></tr>'; // Открываем таблицу шагов сортировки

    for ($i = 0; $i < count($arr) - 1; $i++) { // Перебираем позиции массива, кроме последней
        $min = $i; // Считаем текущую позицию индексом минимального элемента

        for ($j = $i + 1; $j < count($arr); $j++) { // Перебираем оставшуюся неотсортированную часть массива
            if ($arr[$j] < $arr[$min]) { // Если найден элемент меньше текущего минимального
                $min = $j; // Запоминаем индекс нового минимального элемента
            } // Закрываем проверку минимального элемента
        } // Закрываем внутренний цикл поиска минимума

        if ($min !== $i) { // Проверяем, нужно ли менять текущий элемент с найденным минимумом
            $temp = $arr[$i]; // Временно сохраняем значение текущего элемента
            $arr[$i] = $arr[$min]; // На текущую позицию ставим минимальный элемент
            $arr[$min] = $temp; // На место минимального элемента ставим прежний текущий элемент
        } // Закрываем обмен элементов

        printStep($arr, $iteration, 'Поиск минимального элемента для позиции ' . $i); // Выводим состояние массива после очередного шага
    } // Закрываем внешний цикл сортировки выбором

    echo '</table>'; // Закрываем таблицу шагов сортировки
    return $iteration; // Возвращаем количество выполненных итераций
} // Закрываем функцию sorting_by_choice

// Пузырьковая сортировка.
function BubbleSort(array $arr): int // Объявляем функцию пузырьковой сортировки, возвращающую число итераций
{ // Открываем тело функции BubbleSort
    $iteration = 0; // Создаём счётчик итераций и начинаем с нуля
    echo '<table class="steps-table"><tr><th>№ итерации</th><th>Действие</th><th>Состояние массива</th></tr>'; // Открываем таблицу шагов сортировки

    for ($j = 0; $j < count($arr) - 1; $j++) { // Выполняем несколько проходов по массиву
        for ($i = 0; $i < count($arr) - 1 - $j; $i++) { // Перебираем соседние пары в ещё неотсортированной части
            if ($arr[$i] > $arr[$i + 1]) { // Если левый элемент больше правого, порядок нарушен
                $temp = $arr[$i]; // Временно сохраняем левый элемент
                $arr[$i] = $arr[$i + 1]; // Переносим правый элемент влево
                $arr[$i + 1] = $temp; // Переносим сохранённый левый элемент вправо
            } // Закрываем условие обмена соседних элементов

            printStep($arr, $iteration, 'Сравнение элементов с индексами ' . $i . ' и ' . ($i + 1)); // Выводим состояние массива после сравнения пары
        } // Закрываем внутренний цикл прохода по парам
    } // Закрываем внешний цикл пузырьковой сортировки

    echo '</table>'; // Закрываем таблицу шагов сортировки
    return $iteration; // Возвращаем количество выполненных итераций
} // Закрываем функцию BubbleSort

// Сортировка Шелла.
function ShellsSort(array $arr): int // Объявляем функцию сортировки Шелла, возвращающую число итераций
{ // Открываем тело функции ShellsSort
    $iteration = 0; // Создаём счётчик итераций и начинаем с нуля
    echo '<table class="steps-table"><tr><th>№ итерации</th><th>Действие</th><th>Состояние массива</th></tr>'; // Открываем таблицу шагов сортировки

    for ($gap = (int)ceil(count($arr) / 2); $gap >= 1; $gap = (int)ceil($gap / 2)) { // Задаём расстояние между сравниваемыми элементами и постепенно уменьшаем его
        for ($i = $gap; $i < count($arr); $i++) { // Перебираем элементы, начиная с позиции gap
            $val = $arr[$i]; // Сохраняем текущий элемент, который нужно вставить на место
            $j = $i - $gap; // Переходим к предыдущему элементу на расстоянии gap

            while ($j >= 0 && $arr[$j] > $val) { // Пока слева есть элемент больше текущего значения
                $arr[$j + $gap] = $arr[$j]; // Сдвигаем больший элемент вправо на расстояние gap
                $j -= $gap; // Переходим ещё левее на расстояние gap
                printStep($arr, $iteration, 'Сдвиг элемента при расстоянии ' . $gap); // Выводим состояние массива после сдвига
            } // Закрываем цикл сдвига элементов

            $arr[$j + $gap] = $val; // Вставляем сохранённое значение на найденное место
            printStep($arr, $iteration, 'Вставка элемента при расстоянии ' . $gap); // Выводим состояние массива после вставки
        } // Закрываем цикл перебора элементов для текущего расстояния

        if ($gap === 1) { // Проверяем, был ли выполнен последний проход с расстоянием 1
            break; // Прерываем цикл, чтобы не зациклиться из-за ceil(1 / 2) = 1
        } // Закрываем проверку последнего расстояния
    } // Закрываем цикл изменения расстояния gap

    echo '</table>'; // Закрываем таблицу шагов сортировки
    return $iteration; // Возвращаем количество выполненных итераций
} // Закрываем функцию ShellsSort

// Сортировка садового гнома.
function gnomeSort(array $arr): int // Объявляем функцию сортировки садового гнома, возвращающую число итераций
{ // Открываем тело функции gnomeSort
    $iteration = 0; // Создаём счётчик итераций и начинаем с нуля
    $i = 1; // Начинаем со второго элемента массива, так как сравниваем его с предыдущим

    echo '<table class="steps-table"><tr><th>№ итерации</th><th>Действие</th><th>Состояние массива</th></tr>'; // Открываем таблицу шагов сортировки

    while ($i < count($arr)) { // Выполняем цикл, пока индекс не дошёл до конца массива
        if ($i === 0 || $arr[$i - 1] <= $arr[$i]) { // Если предыдущего элемента нет или порядок пары правильный
            printStep($arr, $iteration, 'Порядок верный, гном идет вперед'); // Выводим состояние массива перед шагом вперёд
            $i++; // Двигаемся вправо к следующему элементу
        } else { // Иначе порядок пары нарушен
            $temp = $arr[$i]; // Временно сохраняем текущий элемент
            $arr[$i] = $arr[$i - 1]; // Переносим предыдущий элемент на место текущего
            $arr[$i - 1] = $temp; // Переносим сохранённый текущий элемент на место предыдущего
            printStep($arr, $iteration, 'Порядок нарушен, меняем элементы и идем назад'); // Выводим состояние массива после обмена
            $i--; // Двигаемся на один шаг назад
        } // Закрываем ветвление алгоритма гнома
    } // Закрываем цикл сортировки садового гнома

    echo '</table>'; // Закрываем таблицу шагов сортировки
    return $iteration; // Возвращаем количество выполненных итераций
} // Закрываем функцию gnomeSort

// Внутренняя рекурсивная функция быстрой сортировки.
function quickSortInternal(array &$arr, int $left, int $right, int &$iteration): void // Объявляем рекурсивную функцию быстрой сортировки с передачей массива по ссылке
{ // Открываем тело функции quickSortInternal
    $l = $left; // Копируем левую границу сортируемого участка
    $r = $right; // Копируем правую границу сортируемого участка
    $point = $arr[(int)floor(($left + $right) / 2)]; // Выбираем опорный элемент из середины участка

    do { // Начинаем цикл разбиения массива относительно опорного элемента
        while ($arr[$l] < $point) { // Двигаем левый указатель вправо, пока элемент меньше опорного
            $l++; // Увеличиваем левый указатель
        } // Закрываем цикл движения левого указателя

        while ($arr[$r] > $point) { // Двигаем правый указатель влево, пока элемент больше опорного
            $r--; // Уменьшаем правый указатель
        } // Закрываем цикл движения правого указателя

        if ($l <= $r) { // Если указатели ещё не пересеклись, нужно выполнить обмен
            $temp = $arr[$l]; // Временно сохраняем левый элемент
            $arr[$l] = $arr[$r]; // Ставим правый элемент на левую позицию
            $arr[$r] = $temp; // Ставим сохранённый левый элемент на правую позицию
            printStep($arr, $iteration, 'Обмен элементов. Опорное значение: ' . formatNumber($point)); // Выводим состояние массива после обмена
            $l++; // Сдвигаем левый указатель вправо
            $r--; // Сдвигаем правый указатель влево
        } // Закрываем условие обмена элементов
    } while ($l <= $r); // Повторяем разбиение, пока указатели не пересеклись

    if ($left < $r) { // Проверяем, осталась ли левая часть для сортировки
        quickSortInternal($arr, $left, $r, $iteration); // Рекурсивно сортируем левую часть массива
    } // Закрываем проверку левой части

    if ($l < $right) { // Проверяем, осталась ли правая часть для сортировки
        quickSortInternal($arr, $l, $right, $iteration); // Рекурсивно сортируем правую часть массива
    } // Закрываем проверку правой части
} // Закрываем функцию quickSortInternal

