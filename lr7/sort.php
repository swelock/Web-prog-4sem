    echo '<td>—</td>'; // Выводим прочерк вместо номера итерации
    echo '<td>Встроенная функция sort() сортирует массив без вывода внутренних итераций</td>'; // Поясняем, почему нет промежуточных шагов
    echo '<td>' . renderArray($arr) . '</td>'; // Выводим итоговое состояние отсортированного массива
