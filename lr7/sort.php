} // Закрываем функцию h

// Функция проверяет, что аргумент НЕ является числом.
function arg_is_not_Num(string $arg): bool // Объявляем функцию проверки нечислового значения
{ // Открываем тело функции arg_is_not_Num
    $arg = trim(str_replace(',', '.', $arg)); // Убираем пробелы и заменяем запятую на точку для десятичных чисел

    if ($arg === '') { // Проверяем, не является ли строка пустой
        return true; // Пустая строка не является числом, поэтому возвращаем true
    } // Закрываем проверку пустой строки

    return !is_numeric($arg); // Возвращаем true, если после проверки PHP считает значение не числом
} // Закрываем функцию arg_is_not_Num

// Функция преобразует строку в число.
function toNumber(string $arg): float|int // Объявляем функцию преобразования строки в int или float
{ // Открываем тело функции toNumber
    $arg = str_replace(',', '.', trim($arg)); // Подготавливаем строку: убираем пробелы и заменяем запятую на точку
    $number = (float)$arg; // Преобразуем строку в вещественное число

    if (floor($number) == $number) { // Проверяем, является ли число фактически целым
        return (int)$number; // Если дробной части нет, возвращаем число как int
    } // Закрываем проверку целого числа

    return $number; // Если число дробное, возвращаем его как float
} // Закрываем функцию toNumber

// Функция форматирует число для красивого вывода.
function formatNumber(float|int $number): string // Объявляем функцию форматирования числа для HTML-вывода
{ // Открываем тело функции formatNumber
    if (is_int($number) || floor($number) == $number) { // Проверяем, является ли число целым
        return (string)(int)$number; // Возвращаем целое число без дробной части
    } // Закрываем проверку целого числа

    return rtrim(rtrim(number_format((float)$number, 6, '.', ''), '0'), '.'); // Форматируем дробь и убираем лишние нули справа
} // Закрываем функцию formatNumber

// Функция выводит массив в виде набора блоков.
function renderArray(array $arr): string // Объявляем функцию, которая превращает массив в HTML-разметку
{ // Открываем тело функции renderArray
    $html = '<div class="array-view">'; // Создаём начальный HTML-контейнер для отображения массива

    foreach ($arr as $index => $value) { // Перебираем каждый элемент массива вместе с его индексом
        $html .= '<div class="array-item">'; // Добавляем блок одного элемента массива
        $html .= '<span class="array-index">' . h((string)$index) . '</span>'; // Добавляем индекс элемента с безопасным выводом
        $html .= '<span class="array-value">' . h(formatNumber($value)) . '</span>'; // Добавляем значение элемента с форматированием и безопасным выводом
        $html .= '</div>'; // Закрываем блок одного элемента массива
    } // Закрываем цикл перебора элементов массива

    $html .= '</div>'; // Закрываем общий контейнер массива
    return $html; // Возвращаем готовую HTML-строку
} // Закрываем функцию renderArray

