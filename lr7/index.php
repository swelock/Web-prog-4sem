            var row = table.insertRow(table.rows.length); // Добавляем новую строку в конец таблицы

            var numberCell = row.insertCell(0); // Добавляем первую ячейку новой строки для номера элемента
            numberCell.className = 'number-cell'; // Назначаем ячейке CSS-класс для оформления номера
            setHTML(numberCell, 'Элемент ' + index); // Записываем в ячейку подпись с номером элемента

