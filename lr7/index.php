    <script> // Открываем блок JavaScript-кода
        // Функция устанавливает HTML-содержимое указанного элемента.
        function setHTML(element, txt) { // Объявляем функцию setHTML с параметрами: элемент и HTML-текст
            if (element.innerHTML !== undefined) { // Проверяем, поддерживает ли элемент свойство innerHTML
                element.innerHTML = txt; // Если поддерживает, записываем HTML-текст напрямую в элемент
            } else { // Иначе используем запасной способ вставки HTML
                var range = document.createRange(); // Создаём объект диапазона документа
                range.selectNodeContents(element); // Выбираем всё содержимое нужного элемента
                range.deleteContents(); // Очищаем старое содержимое элемента
                var fragment = range.createContextualFragment(txt); // Преобразуем строку HTML в фрагмент документа
                element.appendChild(fragment); // Добавляем созданный фрагмент внутрь элемента
            } // Закрываем условие проверки innerHTML
        } // Закрываем функцию setHTML

        // Функция добавляет одно новое поле для элемента массива без перезагрузки страницы.
