    'Этап*Описание*Статус*Комментарий#1*Создание массива*Готово*10 элементов#2*Создание функций*Готово*getTR и outTable', // Таблица №8: этапы выполнения работы
    'Номер*Первая ячейка*Вторая ячейка*Третья ячейка#1*Ячейка 1*Ячейка 2*Ячейка 3#2*Ячейка 4*Ячейка 5*Ячейка 6', // Таблица №9: пример обычной таблицы
    'A*B*C*D#A1*B1*C1*D1#A2*B2*C2*D2#A3*B3*C3*D3' // Таблица №10: условные данные
); // Закрываем массив структур таблиц

// Функция для безопасного вывода текста в HTML. 
function safeText($text) // Объявляем функцию safeText, которая принимает один аргумент $text
{ // Открываем тело функции safeText
    return htmlspecialchars((string)$text, ENT_QUOTES, 'UTF-8'); // Преобразуем специальные символы HTML, чтобы текст безопасно выводился на страницу
} // Закрываем функцию safeText

// Функция формирует HTML-код одной строки таблицы. 
function getTR($data, $columns_count) // Объявляем функцию getTR, она принимает строку данных и количество колонок
{ // Открываем тело функции getTR
    // Если строка пустая, HTML-код строки не выводится. 
    if (trim($data) === '') { // Удаляем пробелы из строки и проверяем, пустая ли она
        return ''; // Если строка пустая, возвращаем пустую строку и ничего не выводим
    } // Закрываем условие проверки пустой строки

    $cells = explode('*', $data); // Разбиваем строку на ячейки по символу *
    $result = '<tr>'; // Создаём переменную с началом HTML-строки таблицы

    // Выводим ровно столько ячеек, сколько задано в переменной $columns_count. 
    for ($i = 0; $i < $columns_count; $i++) { // Запускаем цикл от 0 до количества колонок
        $cell_value = isset($cells[$i]) ? $cells[$i] : ''; // Если ячейка существует, берём её значение, иначе подставляем пустую строку
        $result .= '<td>' . safeText($cell_value) . '</td>'; // Добавляем к строке таблицы одну ячейку td с безопасным текстом
    } // Закрываем цикл формирования ячеек

    $result .= '</tr>'; // Добавляем закрывающий тег строки таблицы

    return $result; // Возвращаем готовый HTML-код одной строки таблицы
} // Закрываем функцию getTR

// Функция формирует всю таблицу по заданной структуре
function outTable($structure, $columns_count, $table_number) // Объявляем функцию outTable с тремя аргументами
{ // Открываем тело функции outTable
    echo '<section class="table-block">'; // Выводим HTML-секцию для отдельной таблицы
    echo '<h2>Таблица №' . safeText($table_number) . '</h2>'; // Выводим заголовок второго уровня с номером таблицы

    // Если число колонок неправильное, таблица не выводится.
    if ($columns_count <= 0) { // Проверяем, что количество колонок не нулевое и не отрицательное
        echo '<p class="error-msg">Неправильное число колонок</p>'; // Выводим сообщение об ошибке
        echo '</section>'; // Закрываем HTML-секцию таблицы
        return; // Завершаем работу функции, потому что таблицу выводить нельзя
    } // Закрываем условие проверки количества колонок

    // Если структура полностью пустая, значит строк нет. 
    if ($structure === '') { // Проверяем, является ли структура таблицы пустой строкой
        echo '<p class="error-msg">В таблице нет строк</p>'; // Выводим сообщение, что строк нет
        echo '</section>'; // Закрываем HTML-секцию таблицы
        return; // Завершаем работу функции
    } // Закрываем условие проверки пустой структуры

    $rows = explode('#', $structure); // Разбиваем структуру таблицы на строки по символу #
    $table_rows = ''; // Создаём пустую переменную для накопления HTML-кода всех строк таблицы

    for ($i = 0; $i < count($rows); $i++) { // Запускаем цикл по всем строкам, которые получились после explode
        $table_rows .= getTR($rows[$i], $columns_count); // Формируем HTML-код строки через getTR и добавляем его к общей таблице
    } // Закрываем цикл обработки строк таблицы

    // Если после обработки не получилось ни одной строки с ячейками. // Поясняем проверку ниже
    if ($table_rows === '') { // Проверяем, остался ли HTML-код строк пустым
        echo '<p class="error-msg">В таблице нет строк с ячейками</p>'; // Выводим сообщение, что строк с ячейками нет
        echo '</section>'; // Закрываем HTML-секцию таблицы
        return; // Завершаем работу функции
    } // Закрываем условие проверки отсутствия строк с ячейками

    echo '<table>'; // Выводим открывающий HTML-тег таблицы
    echo $table_rows; // Выводим все заранее сформированные строки таблицы
    echo '</table>'; // Выводим закрывающий HTML-тег таблицы

    echo '</section>'; // Закрываем HTML-секцию таблицы
} // Закрываем функцию outTable
