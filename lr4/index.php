// Число колонок, с которым должны выводиться все таблицы. 
$columns_count = 4; // Задаём фиксированное количество колонок для всех таблиц

// Массив структур таблиц. // Ниже создаётся массив с описанием будущих таблиц
// Символ * разделяет колонки, символ # разделяет строки. 
$structures = array( // Создаём массив, в котором каждая строка — структура отдельной таблицы
    'Фамилия*Имя*Группа*Оценка#Ильин*Кирилл*241-353*5#Петров*Артём*241-353*4', // Таблица №1: данные студентов
